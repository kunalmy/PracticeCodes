package javasessionsmorningbatch.com;

public class MethodDeclarationConcept {
    
//difference between method declaration and method definition
    
    //1.Method declaration(Method prototype)-->(Non Implemented method)
    //syntax
    //  data_type method_name();
    
    //example
    //void abc();
    //int add();
    //float sub();
    //double mul();
    
    
    //2.Method Definition(implemented method)
    
    
    //syntax
    /*
        data_type method_name()
        {
            //statements
        }
    
    */
    //example
    /*
    void show()
    {
        System.out.println("This is show method");
    }
    
    }
    */

}
