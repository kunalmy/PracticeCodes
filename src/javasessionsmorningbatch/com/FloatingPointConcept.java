package javasessionsmorningbatch.com;

public class FloatingPointConcept {

	public static void main(String[] args) {
		 //5.float
        //Size: 4bytes
        //range:(_upto 7 decimal digits)
        //1.9876543
        //one way of declaring the float value
        float f1=122222.33f;
        //Second way of declaring the float value
        float f2=(float)13.33;
        System.out.println(f1);
        System.out.println(f2);
        float f3=(float)1.175494351E-38;
        System.out.println(f3);
        System.out.println(f1+f2);
        
        //
        float f4=3.99f;
        long lf    =(long)f4;
        System.out.println(lf);
        
        
        
        System.out.println("*********************************");
        
        //6.double
        //size:8 bytes-->8*8=64 bits
        //range:upto 16 decimal digits
        double d1=333.1;
        double d2=1111.3334;
        System.out.println(d1);
        System.out.println(d2);
        
        
        
        

	}

}
